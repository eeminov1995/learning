n = int(input())
if n >= 1 and n < 5:
    print(1)
elif n >= 5 and n < 9:
    print(2)
elif n >= 9 and n < 13:
    print(3)
elif n >= 13 and n < 17:
    print(4)
elif n >= 17 and n < 21:
    print(5)
elif n >= 21 and n < 25:
    print(6)
elif n >= 25 and n < 29:
    print(7)
elif n >= 29 and n < 33:
    print(8)
elif n >= 33 and n < 37:
    print(9)
else:
    print("Eror")